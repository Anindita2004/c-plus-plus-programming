#include<iostream>
using namespace std;
int main(){
	int n ,sum;
	cout<<"Enter a positive integer: ";
	cin>>n;
	for(int i=0;i<=n;++i){
		sum=sum+i;
	}
	cout<<"sum= "<<sum<<endl;
	return 0;
}
