#include<iostream>
using namespace std;
int main(){
	int a=10;
	int b=11;
	int Increment =++a; 
	int Decrement=--b;
	cout<<" Increment: "<< Increment <<endl;
	cout<<"Decrement : "<< Decrement <<endl;
	return 0;
	
}//the code ++num; increases the value of num by 1.
//++ increases the value of the operand by 1
//-- decreases it by 1
